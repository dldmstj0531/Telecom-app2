package org.example.model;

public class Agency {
    private int id;
    private String name;


    // Getter
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    // Setter
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
